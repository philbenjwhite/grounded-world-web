/**
 * Generates an orthographic globe SVG centered on the Atlantic Ocean,
 * showing both North America (left) and Europe/Africa (right).
 */
import { writeFileSync } from 'fs';

const CX = 250, CY = 250, R = 200;
const CENTER_LAT = 20, CENTER_LON = -35; // Atlantic-centered

function ortho(lat, lon) {
  const phi = lat * Math.PI / 180;
  const lam = lon * Math.PI / 180;
  const phi0 = CENTER_LAT * Math.PI / 180;
  const lam0 = CENTER_LON * Math.PI / 180;
  const cosc = Math.sin(phi0) * Math.sin(phi) + Math.cos(phi0) * Math.cos(phi) * Math.cos(lam - lam0);
  if (cosc < 0) return null;
  const x = CX + R * Math.cos(phi) * Math.sin(lam - lam0);
  const y = CY - R * (Math.cos(phi0) * Math.sin(phi) - Math.sin(phi0) * Math.cos(phi) * Math.cos(lam - lam0));
  return [Math.round(x * 10) / 10, Math.round(y * 10) / 10];
}

function projectLine(coords) {
  const pts = [];
  for (const [lat, lon] of coords) {
    const p = ortho(lat, lon);
    if (p) pts.push(p);
  }
  if (pts.length < 2) return '';
  return 'M' + pts.map(p => p.join(',')).join(' L');
}

function projectPolygon(coords) {
  const d = projectLine(coords);
  return d ? d + 'Z' : '';
}

// Generate latitude lines
function latLines() {
  const paths = [];
  for (let lat = -60; lat <= 80; lat += 20) {
    const pts = [];
    for (let lon = -180; lon <= 180; lon += 2) {
      const p = ortho(lat, lon);
      if (p) pts.push(p);
      else if (pts.length > 1) {
        paths.push('M' + pts.map(p => p.join(',')).join(' L'));
        pts.length = 0;
      } else { pts.length = 0; }
    }
    if (pts.length > 1) paths.push('M' + pts.map(p => p.join(',')).join(' L'));
  }
  return paths;
}

// Generate longitude lines
function lonLines() {
  const paths = [];
  for (let lon = -180; lon < 180; lon += 30) {
    const pts = [];
    for (let lat = -90; lat <= 90; lat += 2) {
      const p = ortho(lat, lon);
      if (p) pts.push(p);
      else if (pts.length > 1) {
        paths.push('M' + pts.map(p => p.join(',')).join(' L'));
        pts.length = 0;
      } else { pts.length = 0; }
    }
    if (pts.length > 1) paths.push('M' + pts.map(p => p.join(',')).join(' L'));
  }
  return paths;
}

